    @Override
    public void update(Observable obs, Object o1) {
        ClientesDB cliDB = (ClientesDB) obs;
        this.setTitle("Profesi√≥n: " + cliDB.getProfesion());
        graficar(cliDB.getTotalHombres(), cliDB.getTotalMujeres());
        this.jPanel1.setVisible(true);
        
    }
    
    private void graficar(int totalHombres, int totalMujeres) {
        float progresoH= (float)totalHombres/(float)(totalHombres+totalMujeres);
        progresoH = (float)progresoH*100;
        float progresoM=(float)totalMujeres/(float)( totalHombres+totalMujeres);
        progresoM=(float)progresoM*100;
    
        this.barHombre.setValue((int)progresoH);        
        this.barMujeres.setValue((int)progresoM);
        
        this.lbPorcentajeH.setText(String.format("%.2f", progresoH)+"%");
        this.lbPorcentajeM.setText(String.format("%.2f", progresoM)+"%");
        
        
    }
